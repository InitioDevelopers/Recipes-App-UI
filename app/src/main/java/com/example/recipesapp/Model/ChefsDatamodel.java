package com.example.recipesapp.Model;

public class ChefsDatamodel {
    int id;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    String chefsName;
    int chefsImage;


    public String getChefsName() {
        return chefsName;
    }

    public void setChefsName(String chefsName) {
        this.chefsName = chefsName;
    }

    public int getChefsImage() {
        return chefsImage;
    }

    public void setChefsImage(int chefsImage) {
        this.chefsImage = chefsImage;
    }


}
